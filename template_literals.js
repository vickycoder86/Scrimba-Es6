//template literals:-we use back ticks(``) so we can make our varibale value dynamic
//Template literals are a new feature introduced in ECMAScript 2015/ ES6.
//It provides an easy way to create multiline strings and perform string interpolation.
//Template literals are the string literals and allow embedded expressions.

let word1 ="vijay";
let word2 = "jain"
let fullname = `${word1} ${word2}`;

console.log(fullname);//this will print "vijay jain"

